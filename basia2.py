#แสดงภาพ basic2
import cv2
img = cv2.imread("image/Klage.jpeg")

#แสดงภาพ imshow
#img ที่เก็บภาพที่สร้างไว้

cv2.imshow("Output", img)
cv2.waitKey(delay= 5000) #รอให้ผู้ใช้งานกดปุ่มเพื่อปิดหน้าต่าง  ,(ค่าดีเลย์)1000ที่เป็นจํานวนมิลลิวินาที เท่ากัน 1 วินาที 5000 = 5 วินาที
#ถ้าไม่อยากให้ปิดภาพเเลยให้ตั้งค่า delay= 0 หรือใส่เลข 0
cv2.destroyAllWindows()#ปิดหน้าต่าง หรือคืนทรัพยกรให้เครื่อง